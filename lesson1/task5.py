x = input('Введите первую букву от a до z').lower()
y = input('Введите первую букву от a до z').lower()
x1 = ord(x) - ord('a') + 1
y1 = ord(y) - ord('a') + 1
z = abs(x1 - y1) - 1 
print(f'Позиция буквы "{x}" в алфавите {x1}')
print(f'Позиция буквы "{y}" в алфавите {y1}')
print(f'Количество букв между "{x}" и "{y}" = {z}')
